class Car:
    def __init__(self):
        self.running = false
        self.velocity = 0
        self.color = "red"
        self.make = "Corvette"
        self.model = "C4"

    def start():
        self.running = true

    def accelerate():
        if (self.running):
            self.velocity = self.velocity + 1




my_car = Car()
my_car.start()
my_car.accelerate()



